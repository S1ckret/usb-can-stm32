void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  BaseType_t pxHigherPriorityTaskWoken = 0;
  switch (GPIO_Pin) {
  case GPIO_PIN_6: CAN_SetDLC(2); CAN_SetExtId(0x00C0FFEE << 3); CAN_Send(heart);
    break;
  case GPIO_PIN_8: CAN_SetDLC(4); CAN_SetExtId(0x0000ABED << 3); CAN_Send(milk);
    break;
  case GPIO_PIN_9: CAN_SetDLC(8); CAN_SetExtId(0x0000BEEF << 3); CAN_Send(love_milk);
    break;
  case GPIO_PIN_11: CAN_SetDLC(5); CAN_SetExtId(0x000C010F << 3); CAN_Send(color);
    break;
  case GPIO_PIN_15: CAN_SetDLC(1); CAN_SetExtId(0x1FBF9001 << 3); CAN_Send(o);
    break;
  default:
    break;
  }
  LCD_SetPos(0, 0);
  printf("Device. DLC: %u\n", CAN_GetDLC());
  HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_14);
}


uint8_t dataToReceive[8] = {0};
CAN_RxHeaderTypeDef rxHeader;
BaseType_t pxHigherPriorityTaskWoken = 0;
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
  HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &rxHeader, dataToReceive);

  HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12);
  xQueueSendFromISR(queueToUsb, dataToReceive, &pxHigherPriorityTaskWoken);
  xTaskNotifyFromISR(UsbTaskHandle, 2, eSetValueWithOverwrite, &pxHigherPriorityTaskWoken);
}

void HAL_CAN_RxFifo1MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
  HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO1, &rxHeader, dataToReceive);

  HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12);
  xQueueSendFromISR(queueToUsb, dataToReceive, &pxHigherPriorityTaskWoken);
  xTaskNotifyFromISR(UsbTaskHandle, 3, eSetValueWithOverwrite, &pxHigherPriorityTaskWoken);
}
